import java.util.PriorityQueue;

class KthLargest {
    private PriorityQueue<Integer> pq;
    private int k;

    public KthLargest(int k, int[] nums) {
        this.k = k;
        pq = new PriorityQueue<>(); // min-heap

        // Add all numbers from nums
        for (int num : nums) {
            pq.add(num);
            if (pq.size() > k) {
                pq.poll(); // keep only k largest elements
            }
        }
    }
    
    public int add(int val) {
        pq.add(val);
        if (pq.size() > k) {
            pq.poll();
        }
        return pq.peek(); // kth largest is the root
    }
}

